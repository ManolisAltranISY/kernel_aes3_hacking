void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
		if (htim -> Instance == TIM2)
		{
			time_diff = __HAL_TIM_GetCounter(&htim2); // read TIM2 counter value -> number of elapsed clock cycles, using a clock of 108 MHz
			if (time_diff < 22){ // a short time length between edges is calculated at 16 clock cycles (167 ns)
					time_length = S_TIME;
			}
			else if (time_diff < 36){ // a long time length between edges is calculated at 32 clock cycles (334 ns)
					time_length = L_TIME;
			}
			else if (time_diff < 52){ // a long time length violating BMC, indicating presence of preamble length between edges is calculated at 32 clock cycles (334 ns)
					time_length = P_TIME;

			}
			else{
					time_length = FAIL;
			}
			if (preamble_detected == 0)
			{
				// right bit shifting for the latest time lengths
				latest_four_digits[0] = latest_four_digits[1];
				latest_four_digits[1] = latest_four_digits[2];
				latest_four_digits[2] = latest_four_digits[3];
				latest_four_digits[3] = time_length;

				// check if the past four time lengths result in a preamble pattern
				if (memcmp(latest_four_digits, preamble_pattern, sizeof(preamble_pattern)) == 0){
					preamble_detected = 1;
				}
			}
			else // if preamble has been detected
			{
				// depending on which part of the frame we are currently in, we either store the time slots, or ignore the content
			    if (current_stage_of_subframe == POPULATED_SUBFRAME)
			    {
			    		// Converting biphase mark code to a TTL logic
			            if (time_length == S_TIME && previous_state == START_OF_CYCLE)
			            {
			                aes3_buffer[buffer_count] = 1;
			                buffer_count++;
			                previous_state = MID_CYCLE;
			            }
			            else if (time_length == S_TIME && previous_state == MID_CYCLE)
			            {
			                previous_state = START_OF_CYCLE;
			            }
			            else if (time_length == L_TIME)
			            {
			                aes3_buffer[buffer_count] = 0;
			                buffer_count++;
			            }
			            // Only 8 PCM data bits required
			            count++;
			            if (buffer_count>7)
			            {
			                current_stage_of_subframe = EMPTY_SUBFRAME;
			                // Depending on the data bits in the time slots, we set the GPIO pins forming the parallel bus
			                HAL_GPIO_WritePin(GPIOC, PCM_1_Pin, aes3_buffer[0]);
							HAL_GPIO_WritePin(GPIOC, PCM_2_Pin, aes3_buffer[1]);
							HAL_GPIO_WritePin(GPIOC, PCM_3_Pin, aes3_buffer[2]);
							HAL_GPIO_WritePin(GPIOC, PCM_4_Pin, aes3_buffer[3]);
							HAL_GPIO_WritePin(GPIOC, PCM_5_Pin, aes3_buffer[4]);
							HAL_GPIO_WritePin(GPIOC, PCM_6_Pin, aes3_buffer[5]);
							HAL_GPIO_WritePin(GPIOC, PCM_7_Pin, aes3_buffer[6]);
							HAL_GPIO_WritePin(GPIOC, PCM_8_Pin, aes3_buffer[7]);
			            }
			    }
			    else
			    {
			        	count++; //stop for the test
			          	if (count>680) //keep in mind 12*64 = 768 bits - 8 bits from preamble - 16 bits for audio sample in between = 744 and then set it to 700 for a safe margin, at which point preamble detection starts
			           	{
			               		count = 0;
			               		current_stage_of_subframe = POPULATED_SUBFRAME;
			               		preamble_detected = 0;
			           	}
			    }
			}
		__HAL_TIM_SetCounter(&htim2, 0); //reset counter after input capture interrupt occurs, +/- 4 above 12 for short
		}
}

